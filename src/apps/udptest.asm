RX_BUF_SIZE	EQU 1518
REPLY_BUF_SIZE	EQU 256

	MODULE MAIN

; resolve_lib's DNS query frame may reach RESOLVE_MAX_FRAME (320),
; larger than UDP_FRAME_LEN (58); reserve the bigger of the two.
TX_BUF		EQU UDPTEST_IMAGE_END
RX_HDR		EQU TX_BUF + RESOLVE_MAX_FRAME
